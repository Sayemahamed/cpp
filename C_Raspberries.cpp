void solve() {
    long long x, numbers, divisor;cin >> numbers >> divisor;
    long long ans = INT64_MAX;
    while (numbers--) {
        cin >> x;
        ans = min( ans, divisor * ((x + divisor - 1) / divisor) - x );
    }
    cout << ans << endl;
}

