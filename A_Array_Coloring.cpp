void solve() {
    long long siz, x, oddCount = 0;
    cin >> siz;
    for (long long i = 0;i < siz;i++) {
        cin >> x;
        if (x & 1)oddCount++;
    }
    if (oddCount & 1)cout << "NO" << endl;
    else
        cout << "YES" << endl;
}

